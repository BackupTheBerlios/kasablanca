    connect( CwdLineA, SIGNAL( returnPressed() ), this, SLOT( SLOT_EnterCwdA() ) );
    connect( CommandLineB, SIGNAL( returnPressed() ), this, SLOT( SLOT_EnterCommandB() ) );
