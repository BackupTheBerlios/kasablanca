    connect( ActionQuit, SIGNAL( activated() ), this, SLOT( SLOT_Quit() ) );
    connect( ActionAbout, SIGNAL( activated() ), this, SLOT( SLOT_About() ) );
    connect( RefreshButtonA, SIGNAL( clicked() ), this, SLOT( SLOT_UpdateA() ) );
    connect( TransferButtonA, SIGNAL( clicked() ), this, SLOT( SLOT_TransferA() ) );
    connect( TransferButtonB, SIGNAL( clicked() ), this, SLOT( SLOT_TransferB() ) );
